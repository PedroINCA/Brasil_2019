#valor absoluto 
x = -15.5
absoluto = abs(x)
print(absoluto)

#Rounded up
y = 3.8 
rounded_up = ceiling(y)
print(rounded_up)

#Rounded down
z = 3.8 
rounded_down = floor (z)
print(rounded_down)

#Standard rounding

a = 4.483847
rounding_st = round(a, digits = 1)
print(rounding_st)

#square root

b = sqrt(9.99999)
print(b)

#natural logarithm of 16

c = log(16)
print(c)

#base-2 logarithm of 16

d = log(16, base = 2)
print(d)

#base-3 logarithm of 16

e = log(16, base = 3)
print(e)

